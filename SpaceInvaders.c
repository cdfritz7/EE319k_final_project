// ******* Possible Hardware I/O connections*******************
// Slide pot1 pin 1 connected to ground
// Slide pot1 pin 2 connected to PE5/AIN8
// Slide pot1 pin 3 connected to +3.3V 
// Slide pot2 pin 1 connected to ground
// Slide pot2 pin 2 connected to PE4/AIN9
// Slide pot2 pin 3 connected to +3.3V
// fire button 1 connected to PB4
// fire button 2 connected to PB5
// 8*R resistor DAC bit 0 on PB0 (least significant bit)
// 4*R resistor DAC bit 1 on PB1
// 2*R resistor DAC bit 2 on PB2
// 1*R resistor DAC bit 3 on PB3 (most significant bit)
// LED on PB4
// LED on PB5

// Backlight (pin 10) connected to +3.3 V
// MISO (pin 9) unconnected
// SCK (pin 8) connected to PA2 (SSI0Clk)
// MOSI (pin 7) connected to PA5 (SSI0Tx)
// TFT_CS (pin 6) connected to PA3 (SSI0Fss)
// CARD_CS (pin 5) unconnected
// Data/Command (pin 4) connected to PA6 (GPIO), high for data, low for command
// RESET (pin 3) connected to PA7 (GPIO)
// VCC (pin 2) connected to +3.3 V
// Gnd (pin 1) connected to ground


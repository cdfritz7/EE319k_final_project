int main1(void){
  SYSCTL_RCGCGPIO_R |= 0x28;	//activate clock for portf
	while((SYSCTL_RCGCGPIO_R & 0x28)==0){};	//checks if port is ready
	GPIO_PORTF_AMSEL_R &= ~0x1F; 			//Disable analog on PE0-5
	GPIO_PORTF_PCTL_R &= ~0x000FFFFF; //enable regular GPIO
	GPIO_PORTF_AFSEL_R &= ~0x1F; 			//regular function on PE0-5
	GPIO_PORTF_DIR_R |= 0x0E;		//output on PF1, PF2, and PF3
	GPIO_PORTF_DEN_R |= 0x0E;		//digital enable on PF1 2 3
  DisableInterrupts();
  buttons_Init();
	EnableInterrupts();

	while(1){
		if(player1button == 1){
			 GPIO_PORTF_DATA_R |= 0x04;
			 Delay100ms(1);
			 player1button = 0;
		}else{
			GPIO_PORTF_DATA_R &= ~0x04;
		}
		if(player2button == 1){
			GPIO_PORTF_DATA_R |= 0x02;
			Delay100ms(1);
			player2button = 0;
		}else{
			GPIO_PORTF_DATA_R &= ~0x02;
		}
	}
}
		

